# 获取随机节日事件
def 获取随机节日事件(节日名):
    """获取指定节日的随机事件
    
    参数:
        节日名(str): 节日名称
    
    返回:
        tuple: (事件名, 事件数据)，若无事件则返回(None, None)
    """
    import random
    
    节日事件 = 获取节日事件(节日名)
    if not 节日事件:
        return (None, None)
    
    事件名 = random.choice(list(节日事件.keys()))
    return (事件名, 节日事件[事件名]) 